public class ejercicio4 {
    public static void main(String[] args) {
        // definiendo variables
                int X = 10; 
                int Y = 5;  
                int N = 3;  
                int M = 2;  
        
                // a. El valor de cada variable
                System.out.println("X = " + X);
                System.out.println("Y = " + Y);
                System.out.println("N = " + N);
                System.out.println("M = " + M);
        
                // b. La suma X + Y
                System.out.println("Suma X + Y = " + (X + Y));
        
                // c. La diferencia X - Y
                System.out.println("Diferencia X - Y = " + (X - Y));
        
                // d. El producto X * Y
                System.out.println("Producto X * Y = " + (X * Y));
        
                // e. El cociente X / Y
                System.out.println("Cociente X / Y = " + (X / Y));
        
                // f. El resto X % Y
                System.out.println("Resto X % Y = " + (X % Y));
        
                // g. La suma N + M
                System.out.println("Suma N + M = " + (N + M));
        
                // h. La diferencia N - M
                System.out.println("Diferencia N - M = " + (N - M));
        
                // i. El producto N * M
                System.out.println("Producto N * M = " + (N * M));
        
                // j. El cociente N / M
                System.out.println("Cociente N / M = " + (N / M));
        
                // k. El resto N % M
                System.out.println("Resto N % M = " + (N % M));
        
                // l. La suma X + N
                System.out.println("Suma X + N = " + (X + N));
        
                // m. El cociente Y / M
                System.out.println("Cociente Y / M = " + (Y / M));
        
                // n. El resto Y % M
                System.out.println("Resto Y % M = " + (Y % M));
        
                // o. El doble de cada variable
                System.out.println("Doble de X = " + (2 * X));
                System.out.println("Doble de Y = " + (2 * Y));
                System.out.println("Doble de N = " + (2 * N));
                System.out.println("Doble de M = " + (2 * M));
        
                // p. La suma de todas las variables
                System.out.println("Suma de todas las variables = " + (X + Y + N + M));
        
                // q. El producto de todas las variables
                System.out.println("Producto de todas las variables = " + (X * Y * N * M));
            }
       }
        
